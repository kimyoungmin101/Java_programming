package java_project;

public class Page_101 {

	public static void main(String[] args) {
		// TODO 자동 생성된 메소드 스텁
		int rank = 1;
		char medalColor;
		
		switch(rank) {
		case 1 : medalColor = 'G';
		break;
		case 2 : medalColor = 'S';
		break;
		case 3 : medalColor = 'B';
		break;
		default : medalColor = 'A';
		}
		System.out.println(rank + "등의 메달 색깔은 " + medalColor + " 입니다.");
	}
	
}
